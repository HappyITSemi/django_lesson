from django.db import models


class UserProfile(models.Model):

    class Meta:
        managed = True
        verbose_name_plural = 'UserProfile'
        db_table = 'user_profile'

    name = models.CharField(max_length=32, verbose_name='アバター名')
    description = models.TextField(max_length=120, verbose_name='自己紹介')
